fixed_x1, fixed_y1 = None, None

# Fungsi update untuk animasi
def update(frame):
    global fixed_x1, fixed_y1  # Mengakses variabel global

    # Hitung waktu saat ini berdasarkan frame
    time_now = frame / 30  # Asumsikan 30 FPS

    # Filter data keypoints berdasarkan waktu
    kp_data1 = keypoints[keypoints['t'] <= time_now]
    kp_data2 = keypoints2[keypoints2['t'] <= time_now]
    kp_data3 = keypoints3[keypoints3['t'] <= time_now]

    # Ambil koordinat dari keypoint 1 yang tetap hanya sekali (pada frame pertama)
    if fixed_x1 is None and not kp_data1.empty:
        fixed_x1, fixed_y1 = kp_data1.iloc[-1]['pixelx'], kp_data1.iloc[-1]['pixely']

    # Pastikan fixed_x1 dan fixed_y1 sudah diinisialisasi sebelum digunakan
    if fixed_x1 is None or fixed_y1 is None:
        return point1, point2, point3, line1, line2, vertical_line  # Jika belum diinisialisasi, jangan lakukan update

    # Mengatur koordinat keypoint 2 dan 3 relatif terhadap keypoint 1
    if not kp_data2.empty:
        relative_x2, relative_y2 = kp_data2.iloc[-1]['pixelx'], kp_data2.iloc[-1]['pixely']
        # Sesuaikan keypoint 2 untuk mengikuti keypoint 1 dengan sedikit pergeseran
        adjusted_x2 = fixed_x1 + (relative_x2 - fixed_x1) * 0.5  # misal, 50% pergeseran
        adjusted_y2 = fixed_y1 + (relative_y2 - fixed_y1) * 0.5  # misal, 50% pergeseran
    else:
        adjusted_x2, adjusted_y2 = fixed_x1, fixed_y1

    if not kp_data3.empty:
        relative_x3, relative_y3 = kp_data3.iloc[-1]['pixelx'], kp_data3.iloc[-1]['pixely']
        # Sesuaikan keypoint 3 untuk mengikuti keypoint 1 dengan sedikit pergeseran
        adjusted_x3 = fixed_x1 + (relative_x3 - fixed_x1) * 0.5  # misal, 50% pergeseran
        adjusted_y3 = fixed_y1 + (relative_y3 - fixed_y1) * 0.5  # misal, 50% pergeseran
    else:
        adjusted_x3, adjusted_y3 = fixed_x1, fixed_y1

    # Update posisi titik
    point1.set_data_3d(fixed_x1, fixed_y1, z_value)
    point2.set_data_3d(adjusted_x2, adjusted_y2, z_value)
    point3.set_data_3d(adjusted_x3, adjusted_y3, z_value)

    # Update garis penghubung
    line1.set_data_3d([fixed_x1, adjusted_x2], [fixed_y1, adjusted_y2], [z_value, z_value])
    line2.set_data_3d([adjusted_x2, adjusted_x3], [adjusted_y2, adjusted_y3], [z_value, z_value])

    # Update garis vertikal dari keypoint 1 (sekarang sepanjang sumbu X)
    vertical_line.set_data_3d(
        [fixed_x1, fixed_x1 + line_length],  # Sumbu X berubah
        [fixed_y1, fixed_y1],                # Sumbu Y tetap
        [z_value, z_value]                   # Sumbu Z tetap
    )

    return point1, point2, point3, line1, line2, vertical_line

